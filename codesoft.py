x=int(input("Enter the first number:"))
y=int(input("Enter the second number:"))
z=(input("Enter the operand [+,-,/,*]:"))
if(z=="+"):
    print("The sum is =",x+y)
elif(z=="-"):
    print("The difference is =",x-y)
elif(z=="*"):
    print("The product is =",x*y)
elif(z=="/"):
    print("The quotient is =",x/y) 
else:
    print("Invalid Entry")    

         

    
